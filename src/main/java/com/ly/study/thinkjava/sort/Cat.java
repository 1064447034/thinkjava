package com.ly.study.thinkjava.sort;

public class Cat {
	private String color;
	private String from;
	public Cat(String color, String from) {
		this.color = color;
		this.from = from;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	
}
